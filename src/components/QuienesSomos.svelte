                <h2 class="pb-3 fushia-text uppercase">asociación minu</h2>
                <div class="font-sans">
                    <p class="pb-3 "><b>Somos una Asociación Civil, sin fines de lucro, dedicada al desarrollo de programas que promueven la inclusión y la participación de los y las jóvenes en la sociedad.</b> Nuestra misión es implementar proyectos educativos y programas tomando como punto de partida el respeto, la democracia y el pluralismo, promoviendo visiones críticas de la realidad. </p>
                    <p class="pb-3 ">Nuestro trabajo pretende complementar los espacios curriculares de educación formal para comprender, desde una perspectiva más didáctica y participativa, las realidades en la que estamos inmersos como sociedad. Para ello apostamos a programas como las simulaciones cívicas, talleres sobre desinformación y fake news, consumos problemáticos, bullying y grooming, entre otros. También, trabajamos con educadores para acercarles nuevas tendencias educativas y herramientas tecnológicas para que utilicen con sus estudiantes.</p>
